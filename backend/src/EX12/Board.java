package EX12;

public class Board {
	String title;
	String content;
	String writer;
	String password;
	int view;
	
	//생성자
	public Board() {}

	public Board(String title, String content, String writer, String password, int view) {
		super();
		this.title = title;
		this.content = content;
		this.writer = writer;
		this.password = password;
		this.view = view;
	}
	
	       
	
}
